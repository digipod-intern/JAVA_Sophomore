// Write a program to check the largest between the three numbers.
/* Relational Operators gives output as boolean values. */
public class largest {
    public static void main(String[] args) {
        int a = 23;
        int b = 56;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }

    public void largest() {}
}